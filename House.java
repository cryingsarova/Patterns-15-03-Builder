public class House {
    protected  int floor ;
    protected int statyes ;

    public House() {
        this.floor = 2;
        this.statyes = 0;
    }

    public int getFloor() {
        return floor;
    }

    public void setFloor(int floor) {
        this.floor = floor;
    }

    public int getStatyes() {
        return statyes;
    }

    public void setStatyes(int statyes) {
        this.statyes = statyes;
    }
}
